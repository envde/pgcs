namespace PgCs.Core.Types.Base;

/// <summary>
/// Стратегия партиционирования таблицы PostgreSQL
/// </summary>
/// <remarks>
/// Партиционирование позволяет физически разделить большую таблицу на меньшие части
/// для улучшения производительности и управляемости данными.
/// </remarks>
public enum PgPartitionStrategy
{
    /// <summary>
    /// RANGE - партиционирование по диапазонам значений
    /// </summary>
    /// <example>
    /// Разделение по датам: 2024-01-01 to 2024-01-31, 2024-02-01 to 2024-02-29
    /// Разделение по ID: 1-1000, 1001-2000, 2001-3000
    /// </example>
    /// <remarks>
    /// Наиболее распространённая стратегия. Подходит для временных рядов и последовательных данных.
    /// </remarks>
    Range,

    /// <summary>
    /// LIST - партиционирование по списку конкретных значений
    /// </summary>
    /// <example>
    /// Разделение по регионам: ('Europe', 'Asia'), ('Americas'), ('Africa', 'Oceania')
    /// Разделение по статусам: ('active', 'pending'), ('closed', 'cancelled')
    /// </example>
    /// <remarks>
    /// Подходит для категориальных данных с известным набором значений.
    /// </remarks>
    List,

    /// <summary>
    /// HASH - партиционирование по хэшу значения
    /// </summary>
    /// <example>
    /// Равномерное распределение по user_id: HASH(user_id) MODULUS 4
    /// </example>
    /// <remarks>
    /// Обеспечивает равномерное распределение данных между партициями.
    /// Не подходит для запросов по диапазонам.
    /// </remarks>
    Hash
}

namespace PgCs.Core.Types.Base;

/// <summary>
/// Уровень гранулярности срабатывания триггера
/// </summary>
public enum PgTriggerLevel
{
    /// <summary>
    /// ROW - триггер срабатывает для КАЖДОЙ затронутой строки
    /// </summary>
    /// <remarks>
    /// - Выполняется N раз для операции, затрагивающей N строк
    /// - Имеет доступ к переменным OLD и NEW (конкретная строка)
    /// - Более медленный, но более гибкий
    /// - По умолчанию для BEFORE/AFTER триггеров
    /// </remarks>
    Row,

    /// <summary>
    /// STATEMENT - триггер срабатывает ОДИН РАЗ для всей операции
    /// </summary>
    /// <remarks>
    /// - Выполняется 1 раз независимо от количества затронутых строк
    /// - Не имеет доступа к конкретным строкам (OLD/NEW недоступны)
    /// - Более быстрый, подходит для логирования операций
    /// - Обязателен для TRUNCATE
    /// </remarks>
    Statement
}

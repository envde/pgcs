namespace PgCs.Core.Types.Base;

/// <summary>
/// Режим параметра функции PostgreSQL (направление передачи данных)
/// </summary>
public enum PgParameterMode
{
    /// <summary>
    /// IN - входной параметр (передается в функцию)
    /// </summary>
    /// <remarks>
    /// Значение по умолчанию. Параметр может быть использован внутри функции,
    /// но не может быть изменён.
    /// </remarks>
    In,

    /// <summary>
    /// OUT - выходной параметр (возвращается из функции)
    /// </summary>
    /// <remarks>
    /// Параметр возвращается из функции. Значение устанавливается внутри функции.
    /// </remarks>
    Out,

    /// <summary>
    /// INOUT - параметр работает и как входной, и как выходной
    /// </summary>
    /// <remarks>
    /// Параметр может быть использован для чтения и записи.
    /// Начальное значение передаётся в функцию, конечное возвращается.
    /// </remarks>
    InOut,

    /// <summary>
    /// VARIADIC - переменное количество параметров одного типа
    /// </summary>
    /// <remarks>
    /// Позволяет передавать произвольное количество аргументов.
    /// Внутри функции доступны как массив.
    /// </remarks>
    Variadic
}
